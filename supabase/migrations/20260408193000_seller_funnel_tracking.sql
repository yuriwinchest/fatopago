CREATE TABLE IF NOT EXISTS public.seller_funnel_events (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  seller_id UUID NOT NULL REFERENCES public.sellers(id) ON DELETE CASCADE,
  seller_referral_id BIGINT NULL REFERENCES public.seller_referrals(id) ON DELETE SET NULL,
  referred_user_id UUID NULL REFERENCES auth.users(id) ON DELETE SET NULL,
  affiliate_code TEXT NOT NULL,
  visitor_id TEXT NULL,
  event_type TEXT NOT NULL,
  source TEXT NOT NULL DEFAULT 'link',
  path TEXT NULL,
  metadata JSONB NOT NULL DEFAULT '{}'::JSONB,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  CONSTRAINT seller_funnel_events_event_type_check CHECK (
    event_type IN ('link_click', 'invite_visit', 'register_completed', 'pix_generated', 'pix_approved')
  ),
  CONSTRAINT seller_funnel_events_source_check CHECK (
    source IN ('link', 'manual', 'system')
  )
);

CREATE INDEX IF NOT EXISTS seller_funnel_events_seller_idx
  ON public.seller_funnel_events (seller_id, created_at DESC);

CREATE INDEX IF NOT EXISTS seller_funnel_events_type_idx
  ON public.seller_funnel_events (seller_id, event_type, created_at DESC);

CREATE INDEX IF NOT EXISTS seller_funnel_events_referred_user_idx
  ON public.seller_funnel_events (referred_user_id, created_at DESC)
  WHERE referred_user_id IS NOT NULL;

CREATE UNIQUE INDEX IF NOT EXISTS seller_funnel_events_unique_visitor_click_idx
  ON public.seller_funnel_events (seller_id, affiliate_code, event_type, visitor_id)
  WHERE event_type IN ('link_click', 'invite_visit') AND visitor_id IS NOT NULL;

CREATE UNIQUE INDEX IF NOT EXISTS seller_funnel_events_unique_registration_idx
  ON public.seller_funnel_events (seller_id, event_type, referred_user_id)
  WHERE event_type = 'register_completed' AND referred_user_id IS NOT NULL;

CREATE UNIQUE INDEX IF NOT EXISTS seller_funnel_events_unique_pix_idx
  ON public.seller_funnel_events (seller_id, event_type, ((metadata ->> 'pix_payment_id')))
  WHERE event_type IN ('pix_generated', 'pix_approved') AND (metadata ? 'pix_payment_id');

ALTER TABLE public.seller_funnel_events ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS seller_funnel_events_admin_select ON public.seller_funnel_events;
CREATE POLICY seller_funnel_events_admin_select
ON public.seller_funnel_events
FOR SELECT
TO authenticated
USING (public.is_admin_user(auth.uid()));

COMMENT ON TABLE public.seller_funnel_events IS
