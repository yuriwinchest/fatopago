CREATE OR REPLACE FUNCTION public.admin_get_seller_report(
  p_seller_id UUID
)
RETURNS JSONB
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_result JSONB;
BEGIN
  PERFORM public.assert_fatopago_admin();

  WITH seller_row AS (
    SELECT
      s.id,
      s.name,
      s.email,
      s.phone,
      s.cpf,
      s.notes,
      s.seller_code,
      s.is_active,
      s.avatar_url,
      s.created_at,
      'https://fatopago.com/convite/' || s.seller_code AS affiliate_link
    FROM public.sellers s
    WHERE s.id = p_seller_id
  ),
  referred_users AS (
    SELECT
      sr.id,
      sr.created_at,
      sr.affiliate_code,
      sr.referred_user_id,
      p.name,
      p.lastname,
      p.email,
      p.city,
      p.state,
      p.avatar_url
    FROM public.seller_referrals sr
    LEFT JOIN public.profiles p
      ON p.id = sr.referred_user_id
    WHERE sr.seller_id = p_seller_id
    ORDER BY sr.created_at DESC
  ),
  sales AS (
    SELECT
      pp.id,
      pp.user_id,
      COALESCE(p.name, 'Usuário') AS referred_name,
      COALESCE(p.lastname, '') AS referred_lastname,
      p.email AS referred_email,
      p.avatar_url AS referred_avatar_url,
      pp.plan_id,
      public.resolve_plan_purchase_amount(pp.plan_id, pp.validation_credit_total) AS amount,
      pp.status,
      pp.created_at
    FROM public.seller_referrals sr
    JOIN public.plan_purchases pp
      ON pp.user_id = sr.referred_user_id
    LEFT JOIN public.profiles p
      ON p.id = pp.user_id
    WHERE sr.seller_id = p_seller_id
      AND COALESCE(pp.status, '') <> 'cancelled'
    ORDER BY pp.created_at DESC
  ),
  funnel_summary AS (
    SELECT
      COUNT(*) FILTER (WHERE sfe.event_type = 'link_click')::INT AS link_clicks,
      COUNT(DISTINCT sfe.visitor_id) FILTER (WHERE sfe.event_type = 'link_click' AND sfe.visitor_id IS NOT NULL)::INT AS unique_link_clicks,
      COUNT(*) FILTER (WHERE sfe.event_type = 'invite_visit')::INT AS invite_visits,
      COUNT(DISTINCT sfe.visitor_id) FILTER (WHERE sfe.event_type = 'invite_visit' AND sfe.visitor_id IS NOT NULL)::INT AS unique_invite_visits,
      COUNT(*) FILTER (WHERE sfe.event_type = 'register_completed')::INT AS registrations,
      COUNT(DISTINCT sfe.referred_user_id) FILTER (WHERE sfe.event_type = 'register_completed' AND sfe.referred_user_id IS NOT NULL)::INT AS unique_registrations,
      COUNT(*) FILTER (WHERE sfe.event_type = 'pix_generated')::INT AS pix_generated,
      COUNT(DISTINCT sfe.referred_user_id) FILTER (WHERE sfe.event_type = 'pix_generated' AND sfe.referred_user_id IS NOT NULL)::INT AS unique_pix_generated,
      COUNT(*) FILTER (WHERE sfe.event_type = 'pix_approved')::INT AS pix_approved,
      COUNT(DISTINCT sfe.referred_user_id) FILTER (WHERE sfe.event_type = 'pix_approved' AND sfe.referred_user_id IS NOT NULL)::INT AS unique_pix_approved
    FROM public.seller_funnel_events sfe
    WHERE sfe.seller_id = p_seller_id
  ),
  funnel_events AS (
    SELECT
      sfe.id,
      sfe.event_type,
      sfe.source,
      sfe.created_at,
      sfe.visitor_id,
      sfe.referred_user_id,
      sfe.seller_referral_id,
      sfe.path,
      sfe.metadata,
      COALESCE(p.name, 'Visitante') AS referred_name,
      COALESCE(p.lastname, '') AS referred_lastname,
      p.email AS referred_email,
      p.city AS referred_city,
      p.state AS referred_state,
      p.avatar_url AS referred_avatar_url,
      NULLIF(sfe.metadata ->> 'plan_id', '') AS plan_id,
      NULLIF(sfe.metadata ->> 'mp_payment_id', '') AS mp_payment_id,
      COALESCE((sfe.metadata ->> 'amount')::NUMERIC, 0) AS amount
    FROM public.seller_funnel_events sfe
    LEFT JOIN public.profiles p
      ON p.id = sfe.referred_user_id
    WHERE sfe.seller_id = p_seller_id
    ORDER BY sfe.created_at DESC
    LIMIT 80
  )
  SELECT jsonb_build_object(
    'seller', COALESCE((SELECT to_jsonb(seller_row) FROM seller_row), '{}'::JSONB),
    'referred_users', COALESCE((SELECT jsonb_agg(to_jsonb(referred_users)) FROM referred_users), '[]'::JSONB),
    'sales', COALESCE((SELECT jsonb_agg(to_jsonb(sales)) FROM sales), '[]'::JSONB),
    'funnel_summary', COALESCE((SELECT to_jsonb(funnel_summary) FROM funnel_summary), '{}'::JSONB),
    'funnel_events', COALESCE((SELECT jsonb_agg(to_jsonb(funnel_events)) FROM funnel_events), '[]'::JSONB)
  )
  INTO v_result;

  RETURN v_result;
END;
$$;

REVOKE ALL ON FUNCTION public.admin_get_seller_report(UUID) FROM PUBLIC;
GRANT EXECUTE ON FUNCTION public.admin_get_seller_report(UUID) TO authenticated;
