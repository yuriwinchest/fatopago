CREATE OR REPLACE FUNCTION public.log_seller_funnel_event(
  p_seller_id UUID,
  p_affiliate_code TEXT,
  p_event_type TEXT,
  p_source TEXT DEFAULT 'link',
  p_visitor_id TEXT DEFAULT NULL,
  p_referred_user_id UUID DEFAULT NULL,
  p_seller_referral_id BIGINT DEFAULT NULL,
  p_path TEXT DEFAULT NULL,
  p_created_at TIMESTAMPTZ DEFAULT NOW(),
  p_metadata JSONB DEFAULT '{}'::JSONB
)
RETURNS BIGINT
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_event_type TEXT := LOWER(TRIM(COALESCE(p_event_type, '')));
  v_source TEXT := LOWER(TRIM(COALESCE(p_source, 'link')));
  v_affiliate_code TEXT := TRIM(COALESCE(p_affiliate_code, ''));
  v_visitor_id TEXT := NULLIF(LEFT(TRIM(COALESCE(p_visitor_id, '')), 120), '');
  v_path TEXT := NULLIF(LEFT(TRIM(COALESCE(p_path, '')), 255), '');
  v_metadata JSONB := COALESCE(p_metadata, '{}'::JSONB);
  v_event_id BIGINT;
  v_existing_id BIGINT;
  v_pix_payment_id TEXT := NULLIF(TRIM(COALESCE(v_metadata ->> 'pix_payment_id', '')), '');
BEGIN
  IF p_seller_id IS NULL THEN
    RETURN NULL;
  END IF;

  IF v_affiliate_code = '' THEN
    RETURN NULL;
  END IF;

  IF v_event_type NOT IN ('link_click', 'invite_visit', 'register_completed', 'pix_generated', 'pix_approved') THEN
    RAISE EXCEPTION 'Tipo de evento de funil inválido: %', p_event_type;
  END IF;

  IF v_source NOT IN ('link', 'manual', 'system') THEN
    v_source := 'link';
  END IF;

  IF p_seller_referral_id IS NULL AND p_referred_user_id IS NOT NULL THEN
    SELECT sr.id
    INTO p_seller_referral_id
    FROM public.seller_referrals sr
    WHERE sr.seller_id = p_seller_id
      AND sr.referred_user_id = p_referred_user_id
    ORDER BY sr.campaign_enabled_at DESC, sr.created_at DESC
    LIMIT 1;
  END IF;

  IF v_event_type IN ('link_click', 'invite_visit') AND v_visitor_id IS NOT NULL THEN
    SELECT sfe.id
    INTO v_existing_id
    FROM public.seller_funnel_events sfe
    WHERE sfe.seller_id = p_seller_id
      AND sfe.affiliate_code = v_affiliate_code
      AND sfe.event_type = v_event_type
      AND sfe.visitor_id = v_visitor_id
    LIMIT 1;

    IF v_existing_id IS NOT NULL THEN
      RETURN v_existing_id;
    END IF;
  END IF;

  IF v_event_type = 'register_completed' AND p_referred_user_id IS NOT NULL THEN
    SELECT sfe.id
    INTO v_existing_id
    FROM public.seller_funnel_events sfe
    WHERE sfe.seller_id = p_seller_id
      AND sfe.event_type = 'register_completed'
      AND sfe.referred_user_id = p_referred_user_id
    LIMIT 1;

    IF v_existing_id IS NOT NULL THEN
      RETURN v_existing_id;
    END IF;
  END IF;

  IF v_event_type IN ('pix_generated', 'pix_approved') AND v_pix_payment_id IS NOT NULL THEN
    SELECT sfe.id
    INTO v_existing_id
    FROM public.seller_funnel_events sfe
    WHERE sfe.seller_id = p_seller_id
      AND sfe.event_type = v_event_type
      AND sfe.metadata ->> 'pix_payment_id' = v_pix_payment_id
    LIMIT 1;

    IF v_existing_id IS NOT NULL THEN
      RETURN v_existing_id;
    END IF;
  END IF;

  INSERT INTO public.seller_funnel_events (
    seller_id,
    seller_referral_id,
    referred_user_id,
    affiliate_code,
    visitor_id,
    event_type,
    source,
    path,
    metadata,
    created_at
  )
  VALUES (
    p_seller_id,
    p_seller_referral_id,
    p_referred_user_id,
    v_affiliate_code,
    v_visitor_id,
    v_event_type,
    v_source,
    v_path,
    v_metadata,
    COALESCE(p_created_at, NOW())
  )
  RETURNING id INTO v_event_id;

  RETURN v_event_id;
END;
$$;

REVOKE ALL ON FUNCTION public.log_seller_funnel_event(UUID, TEXT, TEXT, TEXT, TEXT, UUID, BIGINT, TEXT, TIMESTAMPTZ, JSONB) FROM PUBLIC, anon, authenticated;
GRANT EXECUTE ON FUNCTION public.log_seller_funnel_event(UUID, TEXT, TEXT, TEXT, TEXT, UUID, BIGINT, TEXT, TIMESTAMPTZ, JSONB) TO service_role, postgres;

CREATE OR REPLACE FUNCTION public.track_seller_funnel_event(
  p_affiliate_code TEXT,
  p_event_type TEXT,
  p_visitor_id TEXT DEFAULT NULL,
  p_path TEXT DEFAULT NULL,
  p_metadata JSONB DEFAULT '{}'::JSONB
)
RETURNS JSONB
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_affiliate_code TEXT := TRIM(COALESCE(p_affiliate_code, ''));
  v_event_type TEXT := LOWER(TRIM(COALESCE(p_event_type, '')));
  v_seller_id UUID;
  v_event_id BIGINT;
BEGIN
  IF v_affiliate_code = '' THEN
    RETURN jsonb_build_object('ok', false, 'reason', 'missing_affiliate_code');
  END IF;

  IF v_event_type NOT IN ('link_click', 'invite_visit') THEN
    RAISE EXCEPTION 'Evento público não permitido: %', p_event_type;
  END IF;

  SELECT s.id
  INTO v_seller_id
  FROM public.sellers s
  WHERE s.seller_code = v_affiliate_code
  LIMIT 1;

  IF v_seller_id IS NULL THEN
    RETURN jsonb_build_object('ok', false, 'reason', 'seller_not_found');
  END IF;

  v_event_id := public.log_seller_funnel_event(
    v_seller_id,
    v_affiliate_code,
    v_event_type,
    'link',
    p_visitor_id,
    NULL,
    NULL,
    p_path,
    NOW(),
    COALESCE(p_metadata, '{}'::JSONB)
  );

  RETURN jsonb_build_object(
    'ok', true,
    'tracked', v_event_id IS NOT NULL,
    'event_id', v_event_id,
    'seller_id', v_seller_id
  );
END;
$$;

REVOKE ALL ON FUNCTION public.track_seller_funnel_event(TEXT, TEXT, TEXT, TEXT, JSONB) FROM PUBLIC;
GRANT EXECUTE ON FUNCTION public.track_seller_funnel_event(TEXT, TEXT, TEXT, TEXT, JSONB) TO anon, authenticated;

CREATE OR REPLACE FUNCTION public.handle_seller_referral_funnel_event()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  PERFORM public.log_seller_funnel_event(
    NEW.seller_id,
    NEW.affiliate_code,
    'register_completed',
    COALESCE(NEW.source, 'link'),
    NULL,
    NEW.referred_user_id,
    NEW.id,
    '/register',
    COALESCE(NEW.created_at, NOW()),
    jsonb_build_object(
      'seller_referral_id', NEW.id,
      'source', COALESCE(NEW.source, 'link')
    )
  );

  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS trg_seller_referrals_funnel_after_insert ON public.seller_referrals;
CREATE TRIGGER trg_seller_referrals_funnel_after_insert
AFTER INSERT ON public.seller_referrals
FOR EACH ROW
EXECUTE FUNCTION public.handle_seller_referral_funnel_event();

CREATE OR REPLACE FUNCTION public.handle_pix_payment_funnel_event()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_source TEXT;
  v_payload JSONB;
  v_old_approved BOOLEAN;
  v_new_approved BOOLEAN;
BEGIN
  v_source := CASE
    WHEN TG_OP = 'INSERT' THEN COALESCE(NEW.seller_source, 'system')
    ELSE COALESCE(NEW.seller_source, OLD.seller_source, 'system')
  END;

  IF TG_OP = 'INSERT' THEN
    IF NEW.seller_id IS NULL THEN
      RETURN NEW;
    END IF;

    v_payload := jsonb_build_object(
      'pix_payment_id', NEW.id,
      'mp_payment_id', NEW.mp_payment_id,
      'plan_id', NEW.plan_id,
      'amount', COALESCE(NEW.amount, 0),
      'status', NEW.status
    );

    PERFORM public.log_seller_funnel_event(
      NEW.seller_id,
      (SELECT s.seller_code FROM public.sellers s WHERE s.id = NEW.seller_id),
      'pix_generated',
      v_source,
      NULL,
      NEW.user_id,
      NEW.seller_referral_id,
      '/plans',
      COALESCE(NEW.created_at, NOW()),
      v_payload
    );

    IF COALESCE(LOWER(NEW.status), '') IN ('approved', 'paid', 'completed', 'authorized') OR NEW.plan_activated_at IS NOT NULL THEN
      PERFORM public.log_seller_funnel_event(
        NEW.seller_id,
        (SELECT s.seller_code FROM public.sellers s WHERE s.id = NEW.seller_id),
        'pix_approved',
        v_source,
        NULL,
        NEW.user_id,
        NEW.seller_referral_id,
        '/plans',
        COALESCE(NEW.plan_activated_at, NEW.updated_at, NEW.created_at, NOW()),
        v_payload || jsonb_build_object('approved_at', COALESCE(NEW.plan_activated_at, NEW.updated_at, NEW.created_at, NOW()))
      );
    END IF;

    RETURN NEW;
  END IF;

  v_old_approved := COALESCE(LOWER(OLD.status), '') IN ('approved', 'paid', 'completed', 'authorized') OR OLD.plan_activated_at IS NOT NULL;
  v_new_approved := COALESCE(LOWER(NEW.status), '') IN ('approved', 'paid', 'completed', 'authorized') OR NEW.plan_activated_at IS NOT NULL;

  IF NEW.seller_id IS NULL OR NOT v_new_approved OR v_old_approved THEN
    RETURN NEW;
  END IF;

  v_payload := jsonb_build_object(
    'pix_payment_id', NEW.id,
    'mp_payment_id', NEW.mp_payment_id,
    'plan_id', NEW.plan_id,
    'amount', COALESCE(NEW.amount, 0),
    'status', NEW.status,
    'approved_at', COALESCE(NEW.plan_activated_at, NEW.updated_at, NOW())
  );

  PERFORM public.log_seller_funnel_event(
    NEW.seller_id,
    (SELECT s.seller_code FROM public.sellers s WHERE s.id = NEW.seller_id),
    'pix_approved',
    v_source,
    NULL,
    NEW.user_id,
    NEW.seller_referral_id,
    '/plans',
    COALESCE(NEW.plan_activated_at, NEW.updated_at, NOW()),
    v_payload
  );

  RETURN NEW;
END;
$$;

